/**
 * Scans js/tools/*.js and writes a static tools registry for static hosts (GitHub Pages).
 * Mirrors api/tools-list.php — run after adding or editing @tool metadata in a tool file.
 *
 * Run: node scripts/build-tools-list.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { buildToolsRegistry } from './lib/parse-tool-meta.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

const EXPORT_SKIP_DIRS = new Set(['.git', '.cursor', 'node_modules', 'vendor']);
const EXPORT_ALLOW_EXT = new Set(['html', 'css', 'js', 'json', 'php', 'mjs', 'ps1', 'md', 'mdc']);

function collectExportPaths(baseDir, relPrefix = '') {
    const paths = [];
    for (const name of fs.readdirSync(baseDir, { withFileTypes: true })) {
        if (EXPORT_SKIP_DIRS.has(name.name)) continue;
        const rel = relPrefix ? `${relPrefix}/${name.name}` : name.name;
        const abs = path.join(baseDir, name.name);
        if (name.isDirectory()) {
            paths.push(...collectExportPaths(abs, rel));
            continue;
        }
        const ext = path.extname(name.name).slice(1).toLowerCase();
        if (!EXPORT_ALLOW_EXT.has(ext)) continue;
        if (name.name === '.DS_Store' || name.name === 'Thumbs.db') continue;
        paths.push(rel.replace(/\\/g, '/'));
    }
    return paths;
}

function writeCodeManifest(outJson) {
    const paths = collectExportPaths(ROOT).sort((a, b) => a.localeCompare(b));
    fs.mkdirSync(path.dirname(outJson), { recursive: true });
    fs.writeFileSync(outJson, `${JSON.stringify({ paths }, null, 2)}\n`, 'utf8');
    console.log(`[build-tools-list] ${paths.length} export paths → ${path.relative(ROOT, outJson)}`);
}

const TARGETS = [
    {
        toolsDir: path.join(ROOT, 'js', 'tools'),
        outJson: path.join(ROOT, 'api', 'tools-list.json'),
        outJs: path.join(ROOT, 'js', 'tools', 'registry.js'),
    },
    {
        toolsDir: path.join(ROOT, 'magiclists', 'js', 'tools'),
        outJson: path.join(ROOT, 'magiclists', 'api', 'tools-list.json'),
        outJs: path.join(ROOT, 'magiclists', 'js', 'tools', 'registry.js'),
    },
];

function writeOutputs(tools, { outJson, outJs }) {
    fs.mkdirSync(path.dirname(outJson), { recursive: true });
    fs.mkdirSync(path.dirname(outJs), { recursive: true });

    fs.writeFileSync(outJson, `${JSON.stringify(tools, null, 2)}\n`, 'utf8');

    const js = [
        '/**',
        ' * Auto-generated by scripts/build-tools-list.mjs — do not edit by hand.',
        ' * Rebuild after changing @tool metadata in js/tools/*.js',
        ' */',
        `export const TOOLS_REGISTRY = ${JSON.stringify(tools, null, 4)};`,
        '',
    ].join('\n');

    fs.writeFileSync(outJs, js, 'utf8');
}

for (const target of TARGETS) {
    const tools = buildToolsRegistry(target.toolsDir);
    if (!tools) continue;

    writeOutputs(tools, target);
    console.log(`[build-tools-list] ${tools.length} tools → ${path.relative(ROOT, target.outJs)}`);
}

writeCodeManifest(path.join(ROOT, 'api', 'code-manifest.json'));
